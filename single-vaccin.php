        <h2>La Diphtérie</h2>
          <p><span>- Quand ? :</span>
            la vaccination DTP est simplifiée et ne comporte plus que deux doses, injectées respectivement à l'âge de 2 mois et de 4 mois, suivies d'un rappel à 11 mois. Des rappels ultérieurs sont recommandés à 6 ans, entre 11 et 13 ans, à 25 ans, 45 ans, 65 ans, puis tous les dix ans chez les adultes de plus de 65 ans. <br>
            <span>- Causes:</span> se transmet par les gouttelettes de salive émises lorsqu’un malade parle, tousse ou crache, mais également par l’intermédiaire d’objets ou d’aliments contaminés. <br>
            <span>- Symptômes :</span> la maladie se manifeste par une angine : irritation de la gorge, fièvre, gonflement des ganglions du cou. <br>
            <span>- Conséquences :</span>
            inflammation du muscle cardiaque ou myocardite (impose la surveillance régulière de l'électrocardiogramme) ; Troubles neurologiques : principalement des paralysies diverses se manifestant par des troubles de la déglutition et de la dépression respiratoire ; Inflammation du foie ; Altération de la fonction rénale.
          </p>

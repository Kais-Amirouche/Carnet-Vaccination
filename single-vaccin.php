      <div class="sectionx">
        <h2>Le Tétanos</h2>
          <p><span>- Quand  ?:</span>
             aux bébés dès l’âge de 2 mois;
             aux enfants (à 12 mois, puis entre 4 et 7 ans);
             aux adolescents (11-13 ans);
             aux jeunes adultes de 25 à 29 ans;
             aux adultes et aux seniors (tous les 20 ans pour les vaccins reçus entre 25 et 64 ans, puis tous les 10 ans).
             <br>
             <span>- Causes :</span>
             Le tétanos se contracte par l'infection d'une plaie ou d'une blessure par des spores de la bactérie Clostridium tetani. <br>
             <span>- Symptômes : </span>
             crampes au niveau des mâchoires ou incapacité à ouvrir la bouche ;
             spasmes musculaires souvent dans le dos, l’abdomen et les extrémités ;
             spasmes musculaires soudains et douloureux, souvent déclenchés par des bruits soudains ;
             difficultés à avaler ;
             convulsions ;
             maux de tête ;
             fièvre et transpiration ;
             modification de la tension artérielle ou accélération du rythme cardiaque.
             <br>
             <span>- Conséquences : </span>
             cette maladie cause des spasmes et une paralysie progressive du système musculaire. Le tétanos est fatal dans à peu près 30 % des cas.
          </p>
